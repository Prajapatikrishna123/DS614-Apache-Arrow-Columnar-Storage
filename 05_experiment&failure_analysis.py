"""## The Parameters We Found in options.h

We found two important parameters inside `options.h`
that we can modify directly from Python:

- `compression` → controls how buffers are compressed (None / lz4 / zstd)
- `use_threads` → controls whether Arrow uses multiple CPU cores

We are now going to modify `compression` and observe
what changes in the output file size and write speed.

This is the same kind of change an open source contributor
would make when experimenting with Arrow's IPC behaviour.
"""

import pyarrow as pa
import pyarrow.ipc as ipc
import numpy as np
import time
import os

np.random.seed(42)
n = 100_000

schema = pa.schema([
    pa.field("id",    pa.int32()),
    pa.field("value", pa.float64()),
    pa.field("label", pa.string())
])

batch = pa.record_batch({
    "id"    : np.arange(n, dtype=np.int32).tolist(),
    "value" : np.random.rand(n).tolist(),
    "label" : np.random.choice(["A","B","C","D"], n).tolist()
}, schema=schema)

print("Default IpcWriteOptions parameters:")
print()
default_opts = ipc.IpcWriteOptions()
print(f"  compression          : {default_opts.compression}")
print(f"  use_threads          : {default_opts.use_threads}")
print(f"  use_legacy_format    : {default_opts.use_legacy_format}")
print(f"  allow_64bit          : {default_opts.allow_64bit}")
print()
print("Source: arrow/cpp/src/arrow/ipc/options.h")
print()

# We are modifying the compression parameter
# None = no compression (default)
# 'lz4' = LZ4 fast compression
# 'zstd' = ZSTD better compression

compressions = [
    ("None (default)", None),
    ("LZ4",           "lz4"),
    ("ZSTD",          "zstd"),
]

results = []
baseline_size = None

print("Modifying compression in IpcWriteOptions:")
print()
print(f"{'Compression':>18} | {'File (KB)':>10} | {'Time (ms)':>10} | {'Size change':>12}")
print("-" * 60)

for label, comp in compressions:
    options   = ipc.IpcWriteOptions(compression=comp)
    file_path = f"comp_{label.split()[0]}.arrow"

    start = time.time()
    with ipc.new_file(file_path, schema, options=options) as writer:
        writer.write_batch(batch)
    elapsed = (time.time() - start) * 1000

    size_kb = os.path.getsize(file_path) / 1024

    if baseline_size is None:
        baseline_size = size_kb
        change = "baseline"
    else:
        diff   = size_kb - baseline_size
        change = f"{diff:+.1f} KB"

    results.append((label, size_kb, elapsed))
    print(f"{label:>18} | {size_kb:>10.1f} | {elapsed:>10.2f} | {change:>12}")

print()
print("Smallest file:", min(results, key=lambda x: x[1])[0])
print("Fastest write:", min(results, key=lambda x: x[2])[0])

"""## What the experiment shows

We modified the `compression` parameter in `IpcWriteOptions`.
This parameter is defined in:
`arrow/cpp/src/arrow/ipc/options.h`

**Three modes we tested:**

| Mode | What it does | Tradeoff |
|---|---|---|
| None (default) | Raw bytes, no compression | Fastest write, largest file |
| LZ4 | Fast compression algorithm | Small file, slightly slower |
| ZSTD | Better compression algorithm | Smallest file, slowest write |

**What this proves:**

Arrow exposes compression as a tunable parameter — not a fixed choice.
This is a conscious design decision: Arrow gives the developer control
over the speed vs size tradeoff depending on their use case.

- Streaming pipelines → no compression (speed matters)
- Long term storage → ZSTD (size matters)
- Balanced workloads → LZ4 (middle ground)

By changing one parameter in `IpcWriteOptions` we can
directly observe this tradeoff in file size and write time.

Source: https://arrow.apache.org/docs/python/ipc.html

## What the experiment shows

As we increase alignment from 8 → 512 bytes:

- **File size increases** because Arrow adds padding bytes
  between buffers so each one starts at the correct boundary
- **Write time stays roughly the same** — the extra work
  is just adding padding bytes, not doing more computation

This directly proves Design Decision 4 from our project:

> Zero-copy IPC works because Arrow aligns data at fixed
> memory offsets — but that alignment costs extra bytes

By changing one number in `options.h` we can see
exactly how much extra space alignment costs at each level.

**Default 8 bytes is the right choice for most cases** —
it keeps files small while still guaranteeing alignment
for 64-bit values on all modern CPUs.

# Failure Analysis — From Source Code Knowledge

Now we demonstrate two failure modes.

We know these failure modes exist because we read the source code.
This is the difference between guessing and knowing.

### Failure 1 — Process crash mid-write (no footer)
### Failure 2 — Reading a file written with wrong magic bytes
"""

import pyarrow as pa
import pyarrow.ipc as ipc
import os

schema = pa.schema([
    pa.field("id",    pa.int32()),
    pa.field("value", pa.float64())
])

batch = pa.record_batch({
    "id"    : list(range(100)),
    "value" : [float(i) * 1.1 for i in range(100)]
}, schema=schema)

partial_file = "crash_simulation.arrow"

print("=== Failure 1: Process Crash Mid-Write ===")
print()
print("From writer.cc we know the write sequence:")
print("  Step 1 → WriteSchema()       ✓ runs")
print("  Step 2 → WriteRecordBatch()  ✓ runs")
print("  Step 3 → WriteFileFooter()   ✗ NEVER runs if crash")
print()

writer = ipc.new_file(partial_file, schema)
writer.write_batch(batch)

print("Batch written to disk.")
print("Simulating crash — writer never closed.")
print("WriteFileFooter() never executes.")
print()

del writer

size = os.path.getsize(partial_file)
print(f"File on disk : {partial_file}")
print(f"File size    : {size} bytes")
print(f"Looks like a normal file to the OS.")
print()

print("Now trying to read it back:")
print()

try:
    with ipc.open_file(partial_file) as reader:
        data = reader.get_batch(0)
        print("Read succeeded — file was fine")
except Exception as e:
    print(f"Arrow rejected the file!")
    print(f"Error: {e}")
    print()
    print("Why this happens:")
    print("Arrow reader looks for the footer first.")
    print("Footer tells it where each batch starts in the file.")
    print("No footer = no index = file is completely unreadable.")
    print()
    print("Even though data was physically written to disk,")
    print("Arrow cannot recover it without the footer.")

"""## Failure 1 — What this means in production

This failure mode is very real in big data pipelines.

**Scenario:**
A machine writing Arrow files to shared storage crashes
halfway through. The file exists on disk and has bytes in it.
But when the next job tries to read it — Arrow rejects it.

**Why Arrow has no recovery:**
Arrow's IPC format is designed for speed and simplicity.
The footer-based design means the writer must complete
cleanly for the file to be readable.

**How production systems handle this:**
- Write to a temp file first
- Rename to final name only after `writer.close()` succeeds
- If crash happens — temp file exists, final file does not
- Next job sees no final file and reruns the write

This pattern is called **atomic rename** and it is the
standard way to handle Arrow write failures in production.
"""

