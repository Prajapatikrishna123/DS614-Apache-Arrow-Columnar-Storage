with ipc.new_file('schema_full.arrow', full_schema) as w:
    w.write_batch(full_batch)

print(f"Written file with schema: {full_schema.names}")
print()

# Read only specific columns using column selection
with ipc.open_file('schema_full.arrow') as r:
    batch = r.get_batch(0)
    # Select only 2 columns out of 4
    subset = batch.select(['id', 'salary'])
    print(f"File has columns  : {batch.schema.names}")
    print(f"We selected only  : {subset.schema.names}")
    print()
    print(subset.to_pydict())

# ── Scenario 3: Type mismatch ─────────────────────────────────
