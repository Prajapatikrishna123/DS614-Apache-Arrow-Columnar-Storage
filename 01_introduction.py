"""## Schema — The Blueprint of a Table

A **schema** defines what columns exist and what type each column is.

Arrow enforces the schema strictly.  
If you try to put a string where a number is expected, Arrow will reject it.

This is important for big data pipelines where data comes from  
many different sources — you want a guarantee about the shape of your data.

Docs: https://arrow.apache.org/docs/python/api/types.html
"""

