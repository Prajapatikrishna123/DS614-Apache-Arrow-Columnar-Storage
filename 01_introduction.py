"""## Why does Apache Arrow exist?

Before Arrow, every tool had its own way of storing data in memory.

- **Pandas** stored data one way  
- **Spark** stored data another way  
- **Databases** had their own format  

So when data moved from Spark → Pandas → a database,  
it had to be **converted every single time**.  
That conversion is slow and wastes memory.

Apache Arrow fixes this by giving everyone **one common format**.  
No conversion needed. Everyone reads the same memory directly.

This is called a **standard in-memory columnar format**.

Source: https://arrow.apache.org/overview/
"""

import pyarrow as pa
