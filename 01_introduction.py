print()
print("Salary column :", salaries)

"""## What is a PyArrow Array?

A `pa.array()` is the most basic building block in Arrow.  
Think of it as **one column** of a table.

Each array:
- holds values of a **single data type**
- has a **length** (number of values)
- tracks **null (missing) values** separately using a validity bitmap

We will look at what a validity bitmap is in the next section.

Docs: https://arrow.apache.org/docs/python/generated/pyarrow.Array.html
"""

age_with_null = pa.array([25, 30, None, 40, None])

print("Array        :", age_with_null)
