print("pyarrow version:", pa.__version__)
print("pandas version :", pd.__version__)
print("numpy version  :", np.__version__)

"""## Row-based vs Column-based storage

Most people are familiar with row-based storage (like Excel or Pandas).  
Data is stored one row at a time.

Row 1 → Alice | 25 | 50000

Row 2 → Bob   | 30 | 60000

Row 3 → Carol | 35 | 70000

Arrow stores data **column by column** instead.

Name column   → Alice | Bob | Carol

Age column    → 25    | 30  | 35

Salary column → 50000 | 60000 | 70000

If we want to calculate **average salary**, we only need the salary column.  
Arrow lets us grab just that — without even touching Name or Age.  
That is why columnar storage is faster for analytics.
"""

names    = pa.array(["Alice", "Bob", "Carol", "David"])
ages     = pa.array([25, 30, 35, 40])
salaries = pa.array([50000, 60000, 70000, 80000])

